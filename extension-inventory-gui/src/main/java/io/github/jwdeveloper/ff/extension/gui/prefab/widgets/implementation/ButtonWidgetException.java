package io.github.jwdeveloper.ff.extension.gui.prefab.widgets.implementation;

public class ButtonWidgetException extends RuntimeException
{
    public ButtonWidgetException() {
    }

    public ButtonWidgetException(String message) {
        super(message);
    }

    public ButtonWidgetException(String message, Throwable cause) {
        super(message, cause);
    }

    public ButtonWidgetException(Throwable cause) {
        super(cause);
    }

    public ButtonWidgetException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
